# Перемістити всі нулі до кінця списку

my_list = [1, 44, 0, 360, 0, 714, 0, 1950, 385, 2035, 0, 0, 123, 0, 940]
zeros = my_list.count(0) #створила змінну в якій порахувала усі нулі зі списку

while 0 in my_list:
    my_list.remove(0) #пройшлась циклом по кожному нулі у списку і видалила його

for i in range(zeros): #щоб нулі появились у кінці списку потрібну
    my_list.append(0)  #кількість разів, написала через for

print(my_list)



